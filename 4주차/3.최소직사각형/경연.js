

// 3. 최소 직사각형
// https://school.programmers.co.kr/learn/courses/30/lessons/86491
// * 정렬 알고리즘을 사용할 것

function solution(sizes) {
    const sorted = sizes.map(iarray => iarray.sort((a,b) => a-b));
    const maxf = Math.max(...sorted.map(iarray => iarray[0]));
    const maxl = Math.max(...sorted.map(iarray => iarray[1]));
    
    return maxf * maxl;
}

console(solution[([60, 50], [30, 70], [60, 30], [80, 40])]); // 4000
console(solution[([10, 7], [12, 3], [8, 15], [14, 7], [5, 15])]); //	120
console(solution[([14, 4], [19, 6], [6, 16], [18, 7], [7, 11])]); // 133